    <h1>2. Creative, pleasant, mobile user interface creation</h1>

    <h2>What is mobile UI?</h2>

    <p>I like the example to thinking of it like a <strong>grocery store</strong>. As a shopper, you don't want to think about way finding and have a frustrating experience. You want to go, find what you came for, not wait long, have a pleasant interaction, and be on your way.</p>

    <h2>Why is it important?</h2>

    <p>You want your users to feel comfortable and find what they need no matter the device.By 2011, Apple and Android had announced a combined total of 35 billion apps downloaded. That is a huge amount that has only grown since.</p>

    <h2>How to do it effectively? Creatively?</h2>

    <ul>
      <li>
        <h3>Target Audience</h3>
          <p>Take into account the preferences and interests of those using the site. </p>
      </li>
      <li>
        <h3>Clear navigation is crucial</h3>
          <p>People are coming to you your site for a reason; help them find what they want with minimal effort. (Example: <a href="https://itunes.apple.com/us/app/xfinity-tv-remote/id401629893?mt=8">xfinity TV</a>)</p>
      </li>
        <li>
        <h3>Visual hierarchy</h3>
          <p>Make important things have more weight than others. Keep it simple and effective. &ldquo;Less is more&rdquo; is not a clich&eacute; in mobile app design, but a standard.</p>
      </li>
        <li>
        <h3>Consider conventions</h3>
          <p>Design / Internet (ex: do not underline things that are not links or have call to action buttons without links). Physical actions or gestures are important to consider (swiping, scrolling, vibration feedback, etc.).
          </p>
      </li>
        <li>
        <h3>Typography and color schemes</h3>
          <p>These are important for readability and overall ease on the users eyes and should be an area of focus. (Example: <a href="https://itunes.apple.com/us/app/clear-tasks-reminders-to-do/id493136154?mt=8">Clear</a>)</p>
      </li>
        <li>
        <h3>Scanability</h3>
          <p>Users want to or even unconsciously scan the page to find what they are looking for, often without reading everything. Consider how you use elements such as: images, headers/sub headers, buttons, navigation, and content blocks.</p>
      </li>
        <li>
        <h3>Keep content related</h3>
          <p>Consider grouping, minimal layers or content, and related ads (if they are a must). (Example: <a href="https://play.google.com/store/apps/details?id=com.southwestairlines.mobile&hl=en">Southwest</a>)</p>
      </li>
        <li>
        <h3>Things CANNOT be slow </h3>
          <p>They just cannot. People will leave, or be frustrated and not want to come back.</p>
      </li>
    </ul>

